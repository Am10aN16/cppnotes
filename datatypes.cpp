#include<iostream>
using namespace std;

int main() {

    // Interger Datatype
    int a = 123;
    cout << a << endl;
    cout<< "Size of a is: " << sizeof(a) <<endl;

    //character datatype
    char b = 'N';
    cout<< b << endl;
    cout<< "Size of b is: " << sizeof(b) <<endl;

    //boolean datatype if true print 1 , if false prints 0
    bool bl = true;
    cout<< bl << endl;
     cout<< "Size of bl is: " << sizeof(bl) <<endl;

    //float datatype
    float f = 1.016;
    cout<< f<< endl;
     cout<< "Size of f is: " << sizeof(f) <<endl;

    //double datatype
    double d =10.16;
    cout<< d << endl;
     cout<< "Size of d is: " << sizeof(d) <<endl;


    return 0;
}   

